num1 = float(input("ENTER YOUR FIRST NUMBER : "))
num2 = float(input("ENTER YOUR SECOND NUMBER : "))
op = input("ENTER THE OPERATION THAT YOU WANT : ")
if op == '+' :
    res = num1+num2
    print("sum : ",res)
elif op == '-' :
    res = num1-num2
    print("sum : ",res)
elif op == '*' :
    res = num1*num2
    print("sum : ",res)
elif op == '/' :
    res = num1/num2
    print("sum : ",res)
